package niosSimulator;

public enum niosOpCode {
	call(0x00), jmpi(0X01), ldbu(0x03), addi(0x04), stb(0x05), br(0x06), ldb(0x07), cmpgei(0x08), ldhu(0x0b), andi(0x0c), sth(0x0d), bge(0x0e), ldh(0x0f),
	cmplti(0x10), initda(0x23), ori(0x14), stw(0x15), blt(0x16), ldw(0x17), cmpnei(0x18), flushda(0x1b), xori(0x1c), bne(0x1e), 
	cmpeqi(0x20), ldbuio(0x23), muli(0x24), stbio(0x25), beq(0x26), ldbio(0x27), cmpgeui(0x28), ldhuio(0x2b), andhi(0x2c), sthio(0x2d), bgeu(0x2e), ldhio(0x2f), 
	cmpltui(0x30), custom(0x32), initd(0x33), orhi(0x34), stwio(0x35), bltu(0x36), ldwio(0x37), rdprs(0x38), RTYPE(0x3a), flushd(0x3b), xorhi(0x3c), unknown(0x3f);
	
	
	private int opCodeValue;
	private niosOpCode(int opCodeValue){
		this.opCodeValue = opCodeValue;
	}

	public int getOpCode(){
		return opCodeValue;
	}
	
}
