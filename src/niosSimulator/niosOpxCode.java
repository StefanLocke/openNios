package niosSimulator;

public enum niosOpxCode {
	
	eret(0x01), roli(0x02), rol(0x03), flushp(0x04), ret(0x05), nor(0x06), mulxuu(0x07), cmpge(0x08), bret(0x09), ror(0x0b), flushi(0x0c), jmp(0x0d), and(0x0e), 
	cmplt(0x10), slli(0x12), sll(0x13), wrprs(0x14), or(0x16), mulxsu(0x17), cmpne(0x18), srli(0x1a), srl(0x1b), nextpc(0x1c), callr(0x1d), xor(0x1e), mulxss(0x1f),
	cmpeq(0x20), divu(0x24), div(0x25), rdctl(0x26), mul(0x27), cmpgeu(0x28), initi(0x29), trap(0x2d), wrctl(0x2e), 
	cmpltu(0x30), add(0x31), _break(0x34), sync(0x36), sub(0x39), srai(0x3a), sra(0x3b);

	private int opxCodeValue;
	private niosOpxCode(int opxCodeValue){
		this.opxCodeValue = opxCodeValue;
	}

	public int getOpxCode(){
		return opxCodeValue;
	}
	
}
